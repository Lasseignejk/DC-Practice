// Write a JavaScript program to compute the sum of the two given integers. If the sum is in the range 50-80 return 65. Otherwise, return 80.

function sumOfGivenIntegers(int1, int2) {
    let total = int1 + int2
    if (total >= 50 && total <= 80) {
        return 65
    } else {
        80
    }
}

